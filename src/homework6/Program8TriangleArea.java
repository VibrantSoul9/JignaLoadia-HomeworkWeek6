package homework6;
/*
8. Write a program to calculate the area of a triangle.
 */

public class Program8TriangleArea {
public static void main(String[] args) {
    // enter value of base and height of triangle
    double base = 8;
    double height = 4;
    // Calculate the area of triange
    double area = 0.5 * base * height;
    // Display the calculated area
    System.out.println("The area of the triangle with base " + base + "and height" + height + " is:" + area);
}
}
