package homework6;
/*
9. Write a program to convert the upper case to lower case.
 */

public class Program9UpperToLowerCase {
    public static void main(String[] args){
        // write the text in uppercase
        String uppercaseString = "PRIME IS SUPRIME";
        // Convert uppercase to lowercase
        String lowercaseString = uppercaseString.toLowerCase();
        // Display the converted text
        System.out.println("Uppercase: " + uppercaseString);
        System.out.println("Lowercase: " + lowercaseString);

    }
}
